; ------------------------------------------------------------------------------
; macros for forth 
; ------------------------------------------------------------------------------
macro m_setTOS val
{
       mov dword ptr ebp, val
}

macro m_push val
{
       add ebp, 4
       m_setTOS val
}

; ------------------------------------------------------------------------------
macro m_getTOS val
{
       mov val, dword ptr ebp
}

macro m_pop val
{
       m_getTOS val
       sub ebp, 4
}

; ------------------------------------------------------------------------------
